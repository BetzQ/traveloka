function scrollLeft() {
    // Menggeser slider ke kiri dengan jarak -200
    document.querySelector('.overflow-x-auto').scrollBy({
        right: -200,
        behavior: 'smooth'
    });
}

function scrollRight() {
    // Menggeser slider ke kanan dengan jarak 200
    document.querySelector('.overflow-x-auto').scrollBy({
        left: 200,
        behavior: 'smooth'
    });
}
